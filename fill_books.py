# Словарь для хранения созданных авторов (чтобы не дублировать)
authors_dict = {}


def get_or_create_author(name):
    """Получить или создать автора по имени"""
    name = name.strip()
    if name in authors_dict:
        return authors_dict[name]
    
    author, created = Author.objects.get_or_create(
        name=name,
        defaults={'bio': f'Биография {name} будет добавлена позже'}
    )
    authors_dict[name] = author
    if created:
        print(f"  ✨ Создан автор: {name}")
    else:
        print(f"  📖 Найден автор: {name}")
    return author


def parse_book_line(line):
    """Разобрать строку вида '1. Лев Толстой «Война и мир»'"""
    line = line.strip()
    if not line or not line[0].isdigit():
        return None, None
    
    # Убираем номер и точку
    parts = line.split('.', 1)
    if len(parts) < 2:
        return None, None
    
    rest = parts[1].strip()
    
    # Ищем название в кавычках «...» или "..."
    import re
    title_match = re.search(r'[«"“](.+?)[»"”]', rest)
    
    if title_match:
        title = title_match.group(1)
        # Автор — всё, что до кавычек
        author_name = rest[:title_match.start()].strip()
    else:
        # Если кавычек нет — пробуем разделить по последнему пробелу
        words = rest.split()
        if len(words) >= 2:
            title = words[-1]
            author_name = ' '.join(words[:-1])
        else:
            return None, None
    
    # Очищаем имя автора от лишних символов
    author_name = author_name.rstrip(' -–—')
    
    return author_name, title


